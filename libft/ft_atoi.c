#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int				i;
	int				sinal;
	int				result;
	unsigned char	*str;

	result = 0;
	sinal = 1;
	i = 0;
	str = (unsigned char *)nptr;
	if (nptr[0] <= 0)
		return (0);
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sinal = -sinal;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		result = result * 10 + str[i++] - '0';
	return (sinal * result);
}
